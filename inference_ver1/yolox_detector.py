class YOLOX_DETECTOR:
    def __init__(self):
        self.num_classes = 80
        self.confthre = 0.5
        self.nmsthre  =0.3
        self.start_img_stack = 1

    
    def postprocess(self,prediction, class_agnostic=False):
        box_corner = prediction.new(prediction.shape)
        box_corner[:, :, 0] = prediction[:, :, 0] - prediction[:, :, 2] / 2
        box_corner[:, :, 1] = prediction[:, :, 1] - prediction[:, :, 3] / 2
        box_corner[:, :, 2] = prediction[:, :, 0] + prediction[:, :, 2] / 2
        box_corner[:, :, 3] = prediction[:, :, 1] + prediction[:, :, 3] / 2
        prediction[:, :, :4] = box_corner[:, :, :4]

        output = [None for _ in range(len(prediction))]
        for i, image_pred in enumerate(prediction):
            if not image_pred.size(0):
                continue
            class_conf, class_pred = torch.max(image_pred[:, 5: 5 + self.num_classes], 1, keepdim=True)

            conf_mask = (image_pred[:, 4] * class_conf.squeeze() >= self.confthre).squeeze()
            detections = torch.cat((image_pred[:, :5], class_conf, class_pred.float()), 1)
            detections = detections[conf_mask]
            if not detections.size(0):
                continue

            if class_agnostic:
                nms_out_index = torchvision.ops.nms(
                    detections[:, :4],
                    detections[:, 4] * detections[:, 5],
                    self.nmsthre,
                )
            else:
                nms_out_index = torchvision.ops.batched_nms(
                    detections[:, :4],
                    detections[:, 4] * detections[:, 5],
                    detections[:, 6],
                    nms_thre,
                )

            detections = detections[nms_out_index]
            if output[i] is None:
                output[i] = detections
            else:
                output[i] = torch.cat((output[i], detections))

        return output

    def preproc(self,img, input_size, swap=(2, 0, 1)):
        if len(img.shape) == 3:
            padded_img = np.ones((input_size[0], input_size[1], 3), dtype=np.uint8) * 114
        else:
            padded_img = np.ones(input_size, dtype=np.uint8) * 114

        r = min(input_size[0] / img.shape[0], input_size[1] / img.shape[1])
        resized_img = cv.resize(
            img,
            (int(img.shape[1] * r), int(img.shape[0] * r)),
            interpolation=cv.INTER_LINEAR,
        ).astype(np.uint8)
        padded_img[: int(img.shape[0] * r), : int(img.shape[1] * r)] = resized_img

        padded_img = padded_img.transpose(swap)
        padded_img = np.ascontiguousarray(padded_img, dtype=np.float32)
        return padded_img


    def img_process(self,img):
        img = self.preproc(img,(self.input_w,self.input_h), swap=(2, 0, 1))
        img_list = [img for _ in range(self.start_img_stack)]
        img_stack = np.stack(img_list, axis=0)
        return img_stack




    def make_output(self,result):
        result = np.reshape(result,(self.max_batch,1,-1))
        result = result[0:self.img_batch]
        outputs = torch.Tensor(result)
        outputs = outputs.view([self.img_batch, -1,self.num_classes+5]) 
        outputs = self.postprocess(outputs, class_agnostic=True)
        
        return outputs

    
    def load(self,trt_engine_path):
        #Engine class 소환 
        self.Engine = common.Engine()      
        #inference에 필요한 context 만들어주기  
        self.Engine.make_context(trt_engine_path)
        #버퍼 할당해주기 
        self.max_batch, self.input_w, self.input_h = self.Engine.allocate_buffers_all()


 
    def inference(self,input_data) : 
        result, self.img_batch = self.Engine.do_inference_v2(input_data) #결과 생성
        return result
    
    def release(self):
        self.Engine.flush()

